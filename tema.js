let temacambio=document.querySelector(".temacambio");
function changetheme(a){
    if(a==1){
        temacambio.style.background="black"
    } else if(a==2){
        temacambio.style.background="blue"
    } else if(a==3){
        temacambio.style.background="red"
    } else if(a==4){
        temacambio.style.background="purple"
    } else if(a==5){
        temacambio.style.background="gray"
    }
}













